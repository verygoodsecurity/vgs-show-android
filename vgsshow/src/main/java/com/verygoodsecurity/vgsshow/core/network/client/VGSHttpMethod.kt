package com.verygoodsecurity.vgsshow.core.network.client

/**
 * Set of common HTTP methods that used in Show SDK.
 *
 * @since 1.0.0
 */
enum class VGSHttpMethod {

    /**
     *  HTTP GET method
     */
    GET,

    /**
     *  HTTP POST method
     */
    POST,

    /**
     *  HTTP PUT method
     */
    PUT,

    /**
     *  HTTP PATCH method
     */
    PATCH,

    /**
     *  HTTP DELETE method
     */
    DELETE
}
