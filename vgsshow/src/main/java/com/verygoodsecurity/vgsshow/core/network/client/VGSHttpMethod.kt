package com.verygoodsecurity.vgsshow.core.network.client

/**
 * The set of common HTTP methods used in the Show SDK.
 */
enum class VGSHttpMethod {

    /**
     *  HTTP GET method.
     */
    GET,

    /**
     *  HTTP POST method.
     */
    POST,

    /**
     *  HTTP PUT method.
     */
    PUT,

    /**
     *  HTTP PATCH method.
     */
    PATCH,

    /**
     *  HTTP DELETE method.
     */
    DELETE
}
